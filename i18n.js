const i18n = {
  current: 'ar',

  init() {
    this.current = localStorage.getItem('oryx_lang') || 'ar';
    this.apply();
  },

  t(key, vars = {}) {
    let str = translations[this.current]?.[key] || translations['en']?.[key] || key;
    // Replace {var} placeholders
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), v);
    });
    return str;
  },

  toggle() {
    this.current = this.current === 'ar' ? 'en' : 'ar';
    localStorage.setItem('oryx_lang', this.current);
    this.apply();
    // Dispatch event so app.js can update dynamic content
    window.dispatchEvent(new CustomEvent('langChanged', { detail: this.current }));
  },

  apply() {
    const isAr = this.current === 'ar';
    document.documentElement.lang = isAr ? 'ar' : 'en';
    document.documentElement.dir = isAr ? 'rtl' : 'ltr';
    document.body.classList.toggle('rtl', isAr);

    const btn = document.getElementById('lang-toggle-btn');
    if (btn) btn.textContent = this.t('lang.btn');

    document.querySelectorAll('[data-i18n]').forEach(el => {
      const key = el.getAttribute('data-i18n');
      el.textContent = this.t(key);
    });

    document.querySelectorAll('[data-i18n-ph]').forEach(el => {
      el.placeholder = this.t(el.getAttribute('data-i18n-ph'));
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
      el.innerHTML = this.t(el.getAttribute('data-i18n-html')).replace(/\n/g, '<br>');
    });
  }
};

window.i18n = i18n;
window.t = (key, vars) => i18n.t(key, vars);

// ── LangEngine alias (بعض الصفحات تستخدم window.LangEngine) ──
window.LangEngine = {
  get current() { return i18n.current; },
  t: (key, vars) => i18n.t(key, vars),
  toggle: () => i18n.toggle(),
};

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', () => i18n.init());
} else {
  i18n.init();
}
